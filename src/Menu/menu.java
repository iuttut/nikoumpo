    private void BarHumeurPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_BarHumeurPropertyChange
        BarHumeur.setValue(heros.getHumeurActuel() * 100 / heros.getHumeur());
        if(Dormir.isSelected() == false){
            if(heros.getPvActuel()==0){
            ImageIcon img = new ImageIcon(getClass().getResource("/image/dead.png"));
            Icone.setIcon(img);
            }
            else if((heros.getPvActuel() * 100 / heros.getPv())<5){
                ImageIcon img = new ImageIcon(getClass().getResource("/image/beated.png"));
                Icone.setIcon(img);
            }
            else if(heros.getHumeurActuel()<20){
                ImageIcon img = new ImageIcon(getClass().getResource("/image/choler.png"));
                Icone.setIcon(img);
            }
            else if(heros.getHumeurActuel()<50){
                ImageIcon img = new ImageIcon(getClass().getResource("/image/sad.png"));
                Icone.setIcon(img);
            }
            else if(heros.getHumeurActuel()>=90 && (heros.getPvActuel() * 100 / heros.getPv())>=90){
                ImageIcon img = new ImageIcon(getClass().getResource("/image/sure.png"));
                Icone.setIcon(img);
            }
            else{
                ImageIcon img = new ImageIcon(getClass().getResource("/image/smiley.png"));
                Icone.setIcon(img);
            }
        }
        
        BarHumeur.setString(heros.getHumeurActuel() + "/" + heros.getHumeur());
    }//GEN-LAST:event_BarHumeurPropertyChange

